package parsing.tokenizer

enum class WordType(vararg val atoms: WordAtom): WordDescriptor {
	OPERATOR(
		WordAtom.NOT,
		WordAtom.AND,
		WordAtom.OR,
		WordAtom.INCREMENT,
		WordAtom.DECREMENT,
		WordAtom.PLUS,
		WordAtom.MINUS,
		WordAtom.STAR,
		WordAtom.SLASH,
		WordAtom.ADD,
		WordAtom.SUBTRACT,
		WordAtom.MULTIPLY,
		WordAtom.DIVIDE,
		WordAtom.EQUALS,
		WordAtom.NOT_EQUALS,
		WordAtom.GREATER_OR_EQUALS,
		WordAtom.LOWER_OR_EQUALS,
		WordAtom.GREATER,
		WordAtom.LOWER),
	UNARY_OPERATOR(
		WordAtom.NOT,
		WordAtom.PLUS,
		WordAtom.MINUS),
	UNARY_MODIFICATION(
		WordAtom.INCREMENT,
		WordAtom.DECREMENT),
	BINARY_MODIFICATION(
		WordAtom.ADD,
		WordAtom.SUBTRACT,
		WordAtom.MULTIPLY,
		WordAtom.DIVIDE),
	BINARY_BOOLEAN_OPERATOR(
		WordAtom.AND,
		WordAtom.OR),
	ADDITION(
		WordAtom.PLUS,
		WordAtom.MINUS),
	MULTIPLICATION(
		WordAtom.STAR,
		WordAtom.SLASH),
	EQUALITY(
		WordAtom.EQUALS,
		WordAtom.NOT_EQUALS),
	COMPARISON(
		WordAtom.GREATER_OR_EQUALS,
		WordAtom.LOWER_OR_EQUALS,
		WordAtom.GREATER,
		WordAtom.LOWER),
	MEMBER(
		WordAtom.VAR,
		WordAtom.FUN),
	GENERICS_START(
		WordAtom.LOWER),
	GENERICS_END(
		WordAtom.GREATER),
	TYPE_TYPE(
		WordAtom.CLASS,
		WordAtom.GENERIC,
		WordAtom.OBJECT),
	MODIFIER(
		WordAtom.NATIVE);

	override fun includes(atom: WordAtom?): Boolean {
		return atoms.contains(atom)
	}
}